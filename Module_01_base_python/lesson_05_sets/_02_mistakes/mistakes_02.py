# Тип ошибки 2: Удаление несуществующего элемента — KeyError
#
# Ошибка возникает, если попытаться удалить элемент, которого нет в множестве.
# Метод remove() вызовет ошибку KeyError.

# my_set = {1, 2, 3}
# my_set.remove(4)  # KeyError: 4


# Решение:
#
# Используйте метод discard(), который удаляет элемент, но не вызывает ошибку, если элемента нет.
my_set = {1, 2, 3}
my_set.discard(4)  # Не вызывает ошибку

